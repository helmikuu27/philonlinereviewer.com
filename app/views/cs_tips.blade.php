					<h4 style="text-align: justify;"><strong>Things to remember When Taking Civil Service Examination</strong></h4>
					<ul>
					<li style="text-align: justify;"><strong>Take time to review or self-review days before the examination.</strong>&nbsp; Reviewing will sharpen up your knowledge and will add confidence during the examamination. It will also allow you to rehearse answering hard and unfamiliar questions. Check our online <a href="http://philonlinereviewer.com/Civil-exam">civil service exam reviewer</a>.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Familiarize yourself with the location of the&nbsp;Testing Center</strong>. To familiarize yourself with the location of your assigned school, the CSC urged examinees to do an ocular inspection at least a day or two before examination day.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Eat your &nbsp;breakfast before going to the Testing Center.</strong>&nbsp;The exam is time consuming so a satisfied tummy will help&nbsp;your brain to work at optimum capacity.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Dont forget to bring all the thing that you will be needing during the examination. </strong>Dont forget to bring your application receipt and exam slip, pencil and eraser, black ball pen, valid IDs and of course snacks for your tummy.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Don&rsquo;t be late.</strong> Come earlier like one hour before the time of the exam. It will relax and prepare you well.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Prepare your self.</strong>&nbsp;Befor the exam make sure that you will not need to go the rest room. This will help you not consuming time for the other matters.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Listen to the test instructor.</strong>&nbsp;Always listen to what what he/she will say. They will always tell you what to fill out and what not to fill out. They will also tell you what subject you will take and what time you need to finish on that subject and don&rsquo;t open the Test Booklet until the instructor told you so.&nbsp;</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Exercise your shading techniques. </strong>When shading the circles corresponding to your answers. Don&rsquo;t overshade and don&rsquo;t halfly shade circles&nbsp;or else you would end up failing. You shoul also shade lightly and erase gently to avoid damaging the answer sheet.&nbsp;</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Never leave an item blank</strong> unless the given instruction directs you accordingly.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Make sure you have extra pencils</strong> and that they are already sharpened to avoid sharpening them again and again. Remember, time in the exam is vital.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Don't spend to much time on a single question.</strong> Make sure to manage your time accordingly.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Relax and enjoy the exam.</strong> Avoid panicking because it can only worsen things. If you relax and enjoy the exam, it will make you happy and it will condition your brain.</li>
					</ul>
					<ul>
					<li style="text-align: justify;"><strong>Pray before and after the exam.&nbsp;</strong>&nbsp;</li>
					</ul>

