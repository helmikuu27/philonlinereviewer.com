				<h2><strong>Civil Service Exam: Tips and other Information</strong></h2>
					<p style="text-align: justify;">CSC Professional Eligibility in the Philippines is one of the requirements to be working to many government offices and agencies like GSIS, SSS, BIR etc.&nbsp;The complete <strong>requirements to take the Civil Service Exam</strong> for Professional and Sub-professional levels are the following:</p>
					<ul>
					<li>Must be Filipino citizens</li>
					<li>At least 18 years old</li>
					<li>Of good moral character and have no criminal record or have not been convicted by final judgment of an offense or crime involving moral turpitude;</li>
					<li>Have not been dishonorably discharged from military service or dismissed for cause from any civilian position in the government</li>
					<li>Have not taken the same level of CS exam within 3 months from last examination taken.</li>
					</ul>
					<p>You can also acquire civil service eligibility even without taking the CS exam if you are on the following lists.&nbsp;</p>
