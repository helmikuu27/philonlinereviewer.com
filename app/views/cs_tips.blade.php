					<p><a href="http://excell.csc.gov.ph/ELIGSPECIAL/elig_bhw.html">Barangay Health Worker Eligibility (RA7883)</a></p>
					</li>
					<li>
					<p><a href="http://excell.csc.gov.ph/ELIGSPECIAL/elig_bns.html">Barangay Nutrition Scholar Eligibility (PD1569)</a></p>
