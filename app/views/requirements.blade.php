					school teacher in the Philippines?</b> This question is one of the most common inquiries on the internet about public school teacher.</p>

					<p>The basic requirement in applying for a public school teacher, is your license, 
					which typically requires a bachelor's degree and the completion of an approved 
					teacher education program. This is the first step in joining this vocation,
					a career that gives completion of your dream to be a public servant.</p>

					<p>If you've done your first step. Now is the time for you to complete your goal;
					joining the race! So, if you are a LET passer and wanted to apply for a ranking in public school teachers, 
					the following are the needed requirements:</p>
					<div class="requirements">			
					<li>CSC Form 212 (Revised 2005) in two copies with latest 2"x2" ID picture.</li> 
					<li>Certified photocopy of PRC Certificate of Registration/License.</li>  
					<li>Certified photocopy of ratings obtained in the Licensure Examination for Teachers/Professional Board Examination for Teachers.</li>  
					<li>Certified copies of transcript of records, performance rating and school clearance for those with teaching experience.</li>  
					<li>Certificates of specialized training, if any.</li>  
					<li>Certified copy of the voter's ID.</li>  
					<li>NBI clearance.</li>  
					<li>Omnibus certification of authenticity and veracity of all documents submitted.</li>  
